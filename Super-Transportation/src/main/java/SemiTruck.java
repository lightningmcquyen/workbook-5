public class SemiTruck extends Vehicle {
    private boolean truckTrailer;

    public SemiTruck() {
        this.truckTrailer = truckTrailer;
    }

    public boolean isTruckTrailer() {
        return truckTrailer;
    }

    public void setTruckTrailer(boolean truckTrailer) {
        this.truckTrailer = truckTrailer;
    }
}
